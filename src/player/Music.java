public class Music {

    private String name;

    public Music(String name) {

        this.name = name;
    }

    public Music() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String firstNameProperty() {
        return name;
    }


}
